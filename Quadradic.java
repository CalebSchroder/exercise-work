public class Quadradic
{  
  public static void main ( String[] args )  
  {
    double x;
    double Y;
    x = 2;
    Y = x * x - 2 * x + 8;
    System.out.println(Y);
    x = 3;
    Y = x * x - 2 * x + 8;
    System.out.println(Y);
    x = 4;
    Y = x * x - 2 * x + 8;
    System.out.println(Y);
  }
}